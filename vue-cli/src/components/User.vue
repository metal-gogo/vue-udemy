<template>
    <div class="component">
        <h1>The User Component</h1>
        <p>I'm an awesome User!</p>
        <button @click="changeName">Change my name</button>
        <hr>
        <div class="row">
            <div class="col-xs-12 col-sm-6">
                <app-user-detail 
                    :name="name"
                    :resetFn="resetName"
                    :userAge="age">
                </app-user-detail>
            </div>
            <div class="col-xs-12 col-sm-6">
                <app-user-edit 
                    :userAge="age"
                    :editAge="editAge">
                </app-user-edit>
            </div>
        </div>
    </div>
</template>

<script>
    import UserDetail from './UserDetail.vue';
    import UserEdit from './UserEdit.vue';

    export default {
        components: {
            appUserDetail: UserDetail,
            appUserEdit: UserEdit
        },
        data() {
            return {
                name: 'Metal Gogo',
                age: 28
            };
        },
        methods: {
            changeName() {
                this.name = 'Ale';
            },
            resetName() {
                this.name = 'Metal Gogo';
            },
            editAge() {
                this.age = 30;
            }
        }
    }
</script>

<style scoped lang="scss">
    div {
        background-color: lightblue;
    }
</style>
